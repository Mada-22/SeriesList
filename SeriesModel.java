package org.example;

public class SeriesModel {
    public String seriesID;
    public String seriesName;
    public int seriesAge;
    public String seriesNumberOfEpisode;




    public String getSeriesID() {
        return seriesID;
    }

    public void setSeriesID(String seriesID) {
        this.seriesID = seriesID;
    }

    public String getSeriesName(){
        return seriesName;
    }

    public void setSeriesName(String seriesName) {
        this.seriesName = seriesName;
    }

    public int getSeriesAge() {
        return seriesAge;
    }

    public void setSeriesAge(int seriesAge) {
        this.seriesAge = seriesAge;
    }

    public String getSeriesNumberOfEpisode() {
        return seriesNumberOfEpisode;
    }

    public void setSeriesNumberOfEpisode(String seriesNumberOfEpisode) {
        this.seriesNumberOfEpisode = seriesNumberOfEpisode;
    }


}

